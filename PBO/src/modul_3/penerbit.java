public class penerbit {
    private String nama,alamat,tahun;
    
    public penerbit(String nama,String alamat,String tahun){
        this.nama=nama;
        this.alamat=alamat;
        this.tahun=tahun;
    }

    public String getNama() {
        return nama;
    }

    public String getAlamat() {
        return alamat;
    }
    public String getTahun() {
        return tahun;
    }
    
    
}
